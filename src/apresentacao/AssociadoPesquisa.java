    private void btnFiltrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFiltrarActionPerformed
        try {

            EAssociado parametro = new EAssociado();

            if (cmbFiltro.getSelectedItem().equals("Código")) {
                parametro.setCodigo(Integer.parseInt(txtFiltro.getText()));
            }

            if (cmbFiltro.getSelectedItem().equals("Endereço")) {
                parametro.setEndereco(txtFiltro.getText());
            }

            if (cmbFiltro.getSelectedItem().equals("Telefone")) {
                parametro.setTelefone(txtFiltro.getText());
            }

            List<EAssociado> resultado;

            if (cmbFiltro.getSelectedItem().equals("Nome")) {
                resultado = new NAssociado().listar(txtFiltro.getText());
            } else {
                resultado = new NAssociado().listar(parametro);
            }

            carregarTabela(resultado);

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Erro: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnFiltrarActionPerformed

    private void tblResultadoMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblResultadoMousePressed
        try {

            //Recupera a linha selecionada
            int linha = tblResultado.getSelectedRow();
            String codigoSelecionado
                    = tblResultado.getValueAt(linha, 0).toString();
            
            AssociadoCadastro tela = new AssociadoCadastro(
                    Integer.parseInt(codigoSelecionado));
            
            
            janelaPrincipal.add(tela);
            tela.setVisible(true);
            
            util.Forms.centraliza(tela);
            
            this.hide();
            

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro: " + e.getMessage());
        }
    }//GEN-LAST:event_tblResultadoMousePressed


