    private void carregarTabela(List<EAssociado> resultado) {

        Vector<String> cabecalho = new Vector<String>();
        cabecalho.add("Código");
        cabecalho.add("Nome");
        cabecalho.add("Endereço");
        cabecalho.add("Telefone");

        Vector detalhe = new Vector();
        try {
            for (EAssociado associado : resultado) {

                Vector<String> linha = new Vector<String>();
                linha.add(associado.getCodigo() + "");
                linha.add(associado.getNome());
                linha.add(associado.getEndereco());
                linha.add(associado.getTelefone());
                detalhe.add(linha);
            }
            tblResultado.setModel(new DefaultTableModel(detalhe, cabecalho));

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Erro: " + ex.getMessage());
        }

    }

