    private void carregarCombo() {
        //Primeira aula de agosto - 02/08
        cmbTipoAssociado.removeAll();

        util.ComboItem item0 = new ComboItem("Selecione...", "0");
        cmbTipoAssociado.addItem(item0);

        try {
            NTipoAssociado negocio = new NTipoAssociado();
            for (ETipoAssociado item : negocio.listar()) {

                util.ComboItem cbItem = new ComboItem(item.getDescricao(),
                        String.valueOf(item.getCodigo()));

                cmbTipoAssociado.addItem(cbItem);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        try {
            EAssociado associado = new EAssociado();
            NAssociado negocio = new NAssociado();

            if (!txtCodigo.getText().isEmpty()) {
                associado.setCodigo(Integer.parseInt(txtCodigo.getText()));
            }

            associado.setNome(txtNome.getText());
            associado.setEndereco(txtEndereco.getText());
            associado.setTelefone(txtTelefone.getText());

            //Feito em 02/08
            util.ComboItem itemSelecionado = (util.ComboItem) cmbTipoAssociado.getModel().getSelectedItem();

            if (itemSelecionado.getCodigo().equals("0")) {
                throw new Exception("É necessário informar um tipo de associado.");
            }

            associado.getTipoAssociado().setCodigo(
                    Integer.parseInt(itemSelecionado.getCodigo()));

            negocio.salvar(associado);

            JOptionPane.showMessageDialog(null, "Operação efetuada com sucesso!");

            limpar();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        try {

            String codigo = JOptionPane.showInputDialog(""
                    + "Digite o código do associado para consulta:");

            if (codigo == null || codigo.equals("")) {
                throw new Exception("Digite o código para consulta!");
            }

            int intCodigo = Integer.parseInt(codigo);

            NAssociado negocio = new NAssociado();
            EAssociado associado = negocio.consultar(intCodigo);

            if (associado.getCodigo() != 0) {

                carregarAssociado(associado);

            } else {
                JOptionPane.showMessageDialog(null, "Associado não encontrado!");
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "É necessário informar um número válido.");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        try {
            int resposta = JOptionPane.showConfirmDialog(null,
                    "Confirma a exclusão do Associado?", "iClub",
                    JOptionPane.YES_NO_OPTION);
            if (resposta == JOptionPane.YES_OPTION) {

                NAssociado negocio = new NAssociado();
                negocio.excluir(Integer.parseInt(txtCodigo.getText()));
                JOptionPane.showMessageDialog(null, "Exclusão efetuada com sucesso!");
                limpar();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

