    private void carregarAssociado(EAssociado associado) {
        txtCodigo.setText(String.valueOf(associado.getCodigo()));
        txtNome.setText(associado.getNome());
        txtEndereco.setText(associado.getEndereco());
        txtTelefone.setText(associado.getTelefone());

        //Desafio 1
        int selecionado = 0;

        for (int i = 0; i < cmbTipoAssociado.getModel().getSize(); i++) {
            util.ComboItem item = (util.ComboItem) cmbTipoAssociado.getItemAt(i);

            if (item.getKey().equals(associado.getTipoAssociado().getDescricao())) {
                selecionado = i;
            }
        }

        cmbTipoAssociado.setSelectedItem(cmbTipoAssociado.getItemAt(selecionado));
        //Fim do desafio 1
        
        
        
        btnExcluir.setEnabled(true);
    }
